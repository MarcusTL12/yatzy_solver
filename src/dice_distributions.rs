    fn dice_distr_to_map<const M: usize, const N: usize>(
        x: [([u8; M], u32); N],
    ) -> HashMap<[u8; M], usize> {
        x.iter().enumerate().map(|(i, &(d, _))| (d, i)).collect()
    }

